    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $level = Level::find($id);

        $posts  = $level->posts()
            ->with('category', 'image', 'tags')
            ->withCount('comments')->get();
    
        $videos = $level->videos()
            ->with('category', 'image', 'tags')
            ->withCount('comments')->get();
    
        return view('level', [
            'level'  => $level,
            'posts'  => $posts,
            'videos' => $videos
        ]);
    }

