    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $user = User::find($id);
        $posts  = $user->posts()
		->with('category', 'image', 'tags')
		->withCount('comments')->get();

	$videos = $user->videos()
		->with('category', 'image', 'tags')
		->withCount('comments')->get();

	return view('profile.profile', [
		'user'   => $user,
		'posts'  => $posts,
		'videos' => $videos
	]);
    }

